#include "libft.h"
#include <string.h>

size_t	ft_strlcpy(char *dst, char *src, size_t size)
{
	unsigned int	srclen;
	size_t			i;

	i = 0;
	srclen = ft_strlen(src);
	if (size < 1)
		return (srclen);
	if (size > 0)
	{
		while (i < size - 1 && src[i])
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (srclen);
}
